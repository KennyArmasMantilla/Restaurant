    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
       try {
                
            String n1=buscar.getText();
            PreparedStatement ps=con.prepareStatement("select pedido.idpedido,cliente.idCliente,mesa.idMesa,pedido.FechaPedido,camarero.IDcamarero"
                    + " from cliente inner join pedido on pedido.idcliente=cliente.idCliente "
                +"inner join camarero on pedido.IDcamarero=camarero.IDcamarero "
                    + "inner join mesa on mesa.idMesa=pedido.idMesa "
                      +"where cliente.idCliente='"+n1+"';");
            ResultSet rs =ps.executeQuery();
            ResultSetMetaData rsm =rs.getMetaData();
            ArrayList<String[]> data= new ArrayList<>();
            while(rs.next()){
            String[] rows=new String[rsm.getColumnCount()];
                for (int i = 0; i < rows.length; i++) {
                    rows[i]=rs.getString(i+1);
                    
                } data.add(rows);
            }
            
            
           DefaultTableModel dtm=(DefaultTableModel)this.Tabla.getModel();
            for (int i = 0; i < data.size(); i++) {
                dtm.addRow(data.get(i));
                
            }   
      
        } catch (SQLException ex) {
           JOptionPane.showMessageDialog(null, ex);
        }
        
        
    }//GEN-LAST:event_jButton1ActionPerformed

