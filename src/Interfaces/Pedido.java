     void mostrartabla(){
        DefaultTableModel modelo=new DefaultTableModel();
        modelo.addColumn("idPedido");
        modelo.addColumn("DNI");
        modelo.addColumn("Mesa");
        modelo.addColumn("Fecha de Pedido");
        modelo.addColumn("ID Camarero");
        
        
        Tabla.setModel(modelo);
        
        String sql="select pedido.idpedido,cliente.idCliente,mesa.idMesa,pedido.FechaPedido,camarero.IDcamarero"
                    + " from cliente inner join pedido on pedido.idcliente=cliente.idCliente "
                +"inner join camarero on pedido.IDcamarero=camarero.IDcamarero "
                    + "inner join mesa on mesa.idMesa=pedido.idMesa; ";
        
        String datos[]=new String[7];
        try {
            Statement st = con.createStatement();
            ResultSet rs=st.executeQuery(sql);
            while(rs.next()){
                
            datos[0]=rs.getString(1);
            datos[1]=rs.getString(2);
            datos[2]=rs.getString(3);
            datos[3]=rs.getString(4);
            datos[4]=rs.getString(5);
            
            
            modelo.addRow(datos);
            
            }
            
            Tabla.setModel(modelo);
        } catch (SQLException ex) {
            Logger.getLogger(Usuario.class.getName()).log(Level.SEVERE, null, ex);
        }
        
        
        }
     
